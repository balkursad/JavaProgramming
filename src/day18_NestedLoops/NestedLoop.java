package day18_NestedLoops;

public class NestedLoop {

    public static void main(String[] args) {
        int a=1;
        for (int i = 0; i < 5; i++) {

            for (int j = 0; j < 4; j++) {
                System.out.println(a+ ". Wooden Spoon");
                a++;
            }

        }


    }
}
